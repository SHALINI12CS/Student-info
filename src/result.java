    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
         try{
            stmt = conn.createStatement();
            int stdId = Integer.parseInt(id.getText());
            
            String sql ="SELECT * FROM MARKS WHERE id ='"+stdId+"'";
            rs=stmt.executeQuery(sql);
            
            if(rs.next())
            {
                name.setText(rs.getString("stdName"));
                subject1.setText(String.format("%s",rs.getInt("subject1")));
                subject2.setText(String.format("%s",rs.getInt("subject2")));
                subject3.setText(String.format("%s",rs.getInt("subject")));
                subject4.setText(String.format("%s",rs.getInt("subject4")));
                subject5.setText(String.format("%s",rs.getInt("subject5")));
                percentage.setText(String.format("%s",rs.getInt("percentage")));
                marks.setText(String.format("%s",rs.getInt("marks")));
                grade.setText(String.format("%s",rs.getInt("grade")));
            }else{
                JOptionPane.showMessageDialog(null,"Record not found");
            }
            
        }
        catch(Exception e){JOptionPane.showMessageDialog(null,e);}
    }//GEN-LAST:event_jButton4ActionPerformed

